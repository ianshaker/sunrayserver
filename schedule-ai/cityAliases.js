function normalize(raw) {
  return String(raw || "")
    .trim()
    .toLowerCase()
    .replace(/^г\.?\s+/, "") // «г. Можайск» → «можайск»
    .replace(/^(в|во|по|на|из|от|до)\s+/, "") // «в Можайске», «по Можайску» → «можайске»/«можайску»
    .replace(/ё/g, "е")
    .replace(/[ьъ]/g, "");
}

/** Расстояние Левенштейна — только для опечаток/падежных окончаний, не для семантики. */
function levenshtein(a, b) {
  const m = a.length;
  const n = b.length;
  if (!m) return n;
  if (!n) return m;
  const dp = Array.from({ length: m + 1 }, (_, i) => [i, ...Array(n).fill(0)]);
  for (let j = 0; j <= n; j++) dp[0][j] = j;
  for (let i = 1; i <= m; i++) {
    for (let j = 1; j <= n; j++) {
      dp[i][j] =
        a[i - 1] === b[j - 1]
          ? dp[i - 1][j - 1]
          : 1 + Math.min(dp[i - 1][j], dp[i][j - 1], dp[i - 1][j - 1]);
    }
  }
  return dp[m][n];
}

/**
 * Резолюция города из свободного текста в каноническое значение из CITIES.
 * Допускает падежные формы («в Можайске» → «Можайск») и мелкие опечатки —
 * порог расстояния растёт с длиной названия, чтобы не перепутать разные
 * города (короткие названия — 1 правка, длинные — до 2).
 * @param {string} rawName
 * @returns {{ raw: string, canonical: string|null, found: boolean }}
 */
function resolveCityName(rawName) {
  const norm = normalize(rawName).replace(/[.,!?]+$/g, "");
  if (!norm) return { raw: rawName, canonical: null, found: false };

  const exact = CITIES.find((c) => normalize(c) === norm);
  if (exact) return { raw: rawName, canonical: exact, found: true };

  let best = null;
  for (const city of CITIES) {
    const cityNorm = normalize(city);
    const maxDist = cityNorm.length > 6 ? 2 : 1;
    const dist = levenshtein(norm, cityNorm);
    if (dist <= maxDist && (!best || dist < best.dist)) {
      best = { dist, city };
    }
  }
  if (best) return { raw: rawName, canonical: best.city, found: true };

  return { raw: rawName, canonical: null, found: false };
}

