const EARTH_RADIUS_KM = 6371;

function toRad(deg) {
  return (deg * Math.PI) / 180;
}

/** Расстояние по прямой (км) между двумя точками — формула Хаверсина, без API. */
function haversineKm(lat1, lon1, lat2, lon2) {
  const dLat = toRad(lat2 - lat1);
  const dLon = toRad(lon2 - lon1);
  const a =
    Math.sin(dLat / 2) ** 2 + Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) * Math.sin(dLon / 2) ** 2;
  return EARTH_RADIUS_KM * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

/** null, если у одного из городов нет координат в таблице (не угадываем). */
function distanceBetweenCities(cityA, cityB) {
  const a = CITY_COORDINATES[cityA];
  const b = CITY_COORDINATES[cityB];
  if (!a || !b) return null;
  return haversineKm(a[0], a[1], b[0], b[1]);
}

function hasCoordinates(city) {
  return Boolean(CITY_COORDINATES[city]);
}

