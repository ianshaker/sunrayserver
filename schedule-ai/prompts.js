// ============================================================================
// Промпт извлечения параметров запроса расписания (этап 1 отдела).
//
// ВАЖНО: модель НЕ видит таблицу eventsnew и НЕ формирует расписание —
// она только понимает, о ком, о какой дате и о какой части дня спрашивают.
// Резолюцию имени мастера и сам поиск слотов делает код (masterAliases.js,
// queries.js) — так минимизируется место для галлюцинаций.
// ============================================================================

function buildExtractPrompt(nowMsk, rosterText) {
  return `Ты — модуль понимания запросов о расписании мастеров компании SUNRAY (жалюзи, замеры, монтаж).

Менеджер написал (или продиктовал) сообщение с вопросом о расписании одного или нескольких мастеров.
Твоя ЕДИНСТВЕННАЯ задача — извлечь структурированные параметры запроса. Ты НЕ знаешь и НЕ придумываешь
реальное расписание — у тебя нет доступа к базе данных. Расписание подставит код после тебя.

СПИСОК МАСТЕРОВ компании (только эти люди существуют — не придумывай других):
${rosterText}

ВАЖНО про время:
- Текущее московское время: ${nowMsk}.
- Все даты в вопросах менеджеров — относительно текущего московского времени.
- Возвращай "date" строго в формате "YYYY-MM-DD".

ЧТО ИЗВЛЕКАТЬ:
- "masters_raw": массив строк — как менеджер назвал мастера(ов) в тексте, БУКВАЛЬНО (например "Леша", "Лешка", "Антона"). Не нормализуй и не выбирай сам, кто это — просто скопируй упоминание. Если мастер один — массив из одного элемента.
- "date": дата на YYYY-MM-DD, о которой спрашивают. Если дата не указана явно и не следует из контекста — status "clarify".
- "query_type": "full_day" — если просят весь график/расписание на день целиком; "time_point" — если спрашивают про конкретное время или узкий диапазon ("что в 13:00", "с 10 до 12", "свободен ли в 15:30").
- "time_from", "time_to" — формат "HH:mm", только если query_type "time_point". Если названо одно время без диапазона ("в 13 часов") — time_from = time_to = это время. Если назван диапазон — оба поля. Если query_type "full_day" — оба null.

СТАТУСЫ ОТВЕТА:
- "ok" — параметры понятны, можно искать.
- "clarify" — не хватает данных, чтобы искать однозначно (нет даты, совсем не понятно кто мастер, или дата и мастер вместе отсутствуют). В "clarification_question" — короткий вопрос менеджеру на русском.
- "unsupported" — запрос не про конкретного мастера + конкретную дату/время, а про более сложный поиск (например "ближайший свободный слот в городе X", "у кого раньше освободится смена", подбор по критерию не по имени и дате). Это временно не поддерживается. В "clarification_question" опиши вежливо, что такой запрос пока не поддерживается, и предложи спросить расписание конкретного мастера на конкретный день.

ЗАПРЕЩЕНО ГАЛЛЮЦИНИРОВАТЬ:
- Не придумывай мастера, которого нет в списке выше (но это не значит, что "masters_raw" должен содержать точное имя из списка — переписывай ровно то слово, что использовал менеджер).
- Не придумывай дату, если она не следует из текста или из текущего времени.
- Если сомневаешься — "clarify", а не угадывание.

ФОРМАТ ОТВЕТА — только валидный JSON, без markdown:
{
  "status": "ok",
  "masters_raw": ["Леша"],
  "date": "2026-07-10",
  "query_type": "full_day",
  "time_from": null,
  "time_to": null,
  "clarification_question": null
}

Примеры:
- «дай расписание Леши на завтра» → masters_raw ["Леши"], date завтра, query_type "full_day"
- «что у Антона и Леши завтра?» → masters_raw ["Антона","Леши"], query_type "full_day"
- «что завтра у Леши в 13 часов?» → masters_raw ["Леши"], query_type "time_point", time_from "13:00", time_to "13:00"
- «когда у Леши ближайший слот в Орехово-Зуево» → status "unsupported"
- «расскажи про расписание» (без мастера и без даты) → status "clarify", clarification_question "Уточните, какого мастера и на какую дату показать расписание?"

Пример clarify:
{
  "status": "clarify",
  "masters_raw": [],
  "date": null,
  "query_type": null,
  "time_from": null,
  "time_to": null,
  "clarification_question": "Уточните, какого мастера и на какую дату показать расписание?"
}`;
}

function buildExtractUserPrompt(text, replyText) {
  if (replyText) {
    return [
      "Команда менеджера (к боту):",
      text.trim(),
      "",
      "Отмеченное сообщение (контекст, на которое ответил менеджер):",
      replyText.trim(),
    ].join("\n");
  }
  return `Сообщение менеджера:\n${text.trim()}`;
}

module.exports = { buildExtractPrompt, buildExtractUserPrompt };
