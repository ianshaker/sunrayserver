// ============================================================================
// Промпт извлечения параметров запроса дедлайнов по погрузке (этап 1).
// Модель НЕ видит таблицу eventsnew и НЕ формирует список —
// только понимает дату, лимит и режим. Поиск делает код.
// ============================================================================

function buildExtractPrompt(todayMsk) {
  const year = todayMsk.slice(0, 4);
  return `Ты — модуль понимания запросов менеджера про ДЕДЛАЙНЫ ПО ПОГРУЗКЕ компании SUNRAY.

Менеджер спрашивает, какие события в отделе «Погрузка» (eventsnew, type=Погрузка) имеют deadline на какую-то дату.
Твоя ЕДИНСТВЕННАЯ задача — извлечь параметры запроса. Ты НЕ знаешь и НЕ придумываешь список заявок.

Сегодня (Москва): ${todayMsk}. Год для дат без года — ${year}.
«Сегодня» → ${todayMsk}. «Вчера» → день до сегодня. «Позавчера» → два дня назад.
«Завтра» и будущие даты допустимы (менеджер может спросить заранее).

ЧТО ИЗВЛЕКАТЬ:
- "mode":
  - "by_date" — спросили про конкретную дату / «на сегодня» / просто «дай дедлайны по погрузке» (дата по умолчанию = сегодня).
  - "urgent" — просят самый срочный / важный / «что горит» БЕЗ привязки к одной дате (просроченные + сегодня).
- "date": YYYY-MM-DD для mode "by_date". Если дата не названа — ставь сегодня (${todayMsk}). Для "urgent" всегда null.
- "limit": целое число, сколько карточек показать («дай 7», «покажи десять», «2 заявки»).
  Если не названо — null (код подставит 1). Максимум на стороне кода = 10.
- "all": true только если явно просят «все» / «весь список» / «покажи все». Иначе false.
- "domain_ok": true, если в тексте есть маркеры погрузки/замера-отдела
  (слова: погрузк*, «по погрузке», «по замерам», «дедлайны по замерам», «в отделе погрузки»).
  false — если сказали просто «дедлайны» / «дай 7 дедлайнов» / «что на сегодня» без таких маркеров
  (код в чате погрузки всё равно примет запрос как про погрузку).
- "clarification_question": вопрос менеджеру на русском, если status clarify/unsupported.

СТАТУСЫ:
- "ok" — параметры понятны для поиска.
- "clarify" — не хватает данных. Важно: если mode/date/limit ясны, но domain_ok=false —
  всё равно верни status "ok" с domain_ok=false (уточнение по домену сделает код).
- "unsupported" — просят действие (перенести/назначить/отказ/обновить телефон), а не просмотр;
  или совсем другой сценарий (дедлайны по входящим обращениям). В clarification_question коротко объясни.

ЗАПРЕЩЕНО:
- Придумывать номера заявок или содержимое.
- Менять смысл даты.

ФОРМАТ ОТВЕТА — только валидный JSON, без markdown:
{
  "status": "ok",
  "mode": "by_date",
  "date": "${todayMsk}",
  "limit": null,
  "all": false,
  "domain_ok": true,
  "clarification_question": null
}

Примеры:
- «дай дедлайны по погрузке на сегодня» → mode by_date, date сегодня, domain_ok true, limit null
- «скинь дедлайны по замерам» / «дедлайны по замерам на вчера» → by_date, сегодня/вчера, domain_ok true
- «дай 7 дедлайнов по погрузке» → by_date, сегодня, limit 7, domain_ok true
- «дай 10 дедлайнов на вчера» (без «погрузк*») → by_date, вчера, limit 10, domain_ok false
- «покажи две заявки с дедлайном в погрузке на вчера» → by_date, вчера, limit 2, domain_ok true
- «все дедлайны по погрузке» → by_date, сегодня, all true, domain_ok true
- «есть ли дедлайны по погрузке?» → by_date, сегодня, domain_ok true
- «самый срочный дедлайн по погрузке» → mode urgent, date null, domain_ok true
- «дай дедлайны» / «дай дедлайны на сегодня» → by_date, сегодня, domain_ok false
- «перенеси дедлайн #08044 на завтра» → status unsupported (это действие, не просмотр)
- «дедлайны по входящим» → status unsupported (это другой отдел)`;
}

function buildExtractUserPrompt(text, replyText) {
  if (replyText) {
    return [
      "Команда менеджера (к боту):",
      text.trim(),
      "",
      "Отмеченное сообщение (контекст, на которое ответил менеджер):",
      replyText.trim().slice(0, 600),
    ].join("\n");
  }
  return `Сообщение менеджера:\n${text.trim()}`;
}

module.exports = { buildExtractPrompt, buildExtractUserPrompt };
